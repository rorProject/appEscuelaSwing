    public void recuperarclave(){
        try{
            
            Connection c = conn.conectar();
        
            String passUNO = "";
            String passDOS = "";
        
            String usuario = txtUsuario.getText();
            String email = txtEmail.getText();
            char[] pass1 = txtpass1.getPassword();
            char[] pass2 = txtpass2.getPassword();
        
            for(int i = 0; i<pass1.length;i++){
                passUNO = passUNO + pass1[i];
            }
            for(int i = 0; i<pass2.length;i++){
                passDOS = passDOS + pass2[i];
            }
            
            if(passUNO.equals(passDOS)){
                PreparedStatement recuperar = c.prepareStatement("UPDATE administrador SET contraseña='"+passDOS+"' WHERE usuario='"+usuario+"' AND email='"+email+"'");
                recuperar.execute();
                recuperar.close();
                    
                JOptionPane.showMessageDialog(null, "La contraseña fue actualizada con exito!");
        
                    }else{
                        JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
                    }
                }catch(SQLException e){
                    System.err.println("Ocurrio un error "+e);
                    JOptionPane.showMessageDialog(null, "No existe el ususario o email ingresado");
                }finally{
                    conn.desconectar();
            }
    
    }

