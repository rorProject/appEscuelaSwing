    public void registrar(){
    ConexionBDD conn = new ConexionBDD();
    String pass1 = "";
    String pass2 = "";
    
    String email = txtEmail.getText();
    String usuario = txtUsuario.getText();
    char[] passUNO = txtpass1.getPassword();
    char[] passDOS = txtpass2.getPassword();
    
    for(int i = 0; i<passUNO.length;i++){
            pass1 = pass1 + passUNO[i];
        
        }
    for(int i=0; i<passDOS.length;i++){
            pass2 = pass2 + passDOS[i];
        }
        System.out.println(pass1);
        System.out.println(pass2);
    try{
        Connection c = conn.conectar();
        if(pass1.equals(pass2)){
            PreparedStatement registro = c.prepareStatement("INSERT INTO administrador(usuario, email, contrase単a) values('"+usuario+"','"+email+"','"+pass2+"')");
            registro.execute();
            registro.close();
            JOptionPane.showMessageDialog(null, "El usuario "+usuario+" ha sido creado con exito!","Usuario Nuevo", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Las contrase単as no coinciden, intente nuevamente", "Error", JOptionPane.WARNING_MESSAGE);
        }
        }catch(SQLException e){
            System.err.println("Ocurrio un error "+e );
        }finally{
            conn.desconectar();
        }
    }

